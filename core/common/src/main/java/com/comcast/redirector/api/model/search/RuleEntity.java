package com.comcast.redirector.api.model.search;

import com.comcast.redirector.api.model.Expressions;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAnyElement;
import javax.xml.bind.annotation.XmlRootElement;
import java.util.List;

@XmlRootElement(name = "ruleEntity")
@XmlAccessorType(XmlAccessType.FIELD)
public class RuleEntity {
    private String name;
    private String serviceName;

    @XmlAnyElement(lax = true)
    private List<Expressions> expressions;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }

    public List<Expressions> getExpressions() {
        return expressions;
    }

    public void setExpressions(List<Expressions> expressions) {
        this.expressions = expressions;
    }
}
