package com.comcast.redirector.api.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "percent")
@XmlAccessorType(XmlAccessType.FIELD)
public class Percent extends VisitableExpression implements java.io.Serializable, Expressions {

    @XmlElement(name = "value", required = true)
    private float value;

    public Percent() {}

    public Percent(float value) {
        this.value = value;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof Percent)) return false;

        Percent percent = (Percent) o;

        if (Float.compare(percent.value, value) != 0) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return (value != +0.0f ? Float.floatToIntBits(value) : 0);
    }
}
